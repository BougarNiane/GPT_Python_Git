# str() : 문자열 변환 함수
num = 10
print("숫자 : " + str(num))
#print("숫자 : " + num) : 오류. "" 부분은 str, num은 숫자 타입, 타입이 달라서 오류

# 문자열 반복 연산자 : *
print("여러 번 반복하여 출력\n" * 10)